#include "libft.h"

int		ft_height_end(char **piece)
{
	int i;
	int j;

	i = 3;
	j = 0;
	while (piece[i][j] != '#')
	{
		j++;
		if (j == 4)
		{
			i--;
			j = 0;
		}
	}
	return (i);
}
