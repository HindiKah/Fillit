#include "libft.h"

int		ft_min_h(char **piece)
{
	int		min_h;
	int		i;
	int		j;

	i = 0;
	j = 0;
	while (piece[i][j] != '#' && piece[i])
	{
		if (piece[i][j] == '\0')
		{
			j = 0;
			i++;
		}
		j++;
	}
	min_h = i;
	return (min_h);
}
