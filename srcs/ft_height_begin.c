#include "libft.h"

int		ft_height_begin(char **piece)
{
	int i;
	int j;		

	i = 0;
	j = 0;
	while (piece[i][j] != '#')
	{
		j++;
		if (j == 4)
		{
			i++;
			j = 0;
		}
	}
	return (i);
}
